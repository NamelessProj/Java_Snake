/**
 * Tile class representing a single tile on the game board.
 */
public class Tile {
    public int x;
    public int y;

    /**
     * Constructor for Tile class.
     * @param x The x-coordinate of the tile.
     * @param y The y-coordinate of the tile.
     */
    Tile(int x, int y) {
        this.x = x;
        this.y = y;
    }
}